/*
components of STL:-
    1. Container : Objects which stores data
    2. Algorithms : Procedure to process data . Contains many algorithms (searching, sorting etc)
    3. Iterators : It is an object which points to an element in a container .Connects algorithms with containers. Just like pointer

Types of container:-
    1. Sequence Containers : Stores data in linear Fashion (eg:- Vector, List, Dequeue)
    2. Associative Containers : Used for Fast searching, deletion and access. Stores data in Tree data structure. (eg:- map, set, multiset , multimap)
    3. Derived Containers : (eg: Stack, queue, priority Queue)
*/